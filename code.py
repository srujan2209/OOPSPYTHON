# Creating a Class and Defining it's Attributes Teddy Bear Mold
class Teddy:
    quantity = 200  # This is the property of a class.

# This is the Object
teddy1 = Teddy()
# This is the attribute of a particular object
print(teddy1.quantity)
teddy2 = Teddy()
print(teddy2.quantity)

